package operatorExp;

public class OperatorDemo {

	public static void main(String[] args) {
		// modulo operator (%) that gives reminder
		
		int num1=43,num2=7;
		int rem = num1%num2;
		
		System.out.println(rem);
		
		
		///post increment operator
		int a=12;
		System.out.println(a++);
		
		///post increment operator
		int b =20;
		System.out.println(++b);
		
			
			
		
	}

}
