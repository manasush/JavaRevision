package operatorExp;

public class RelationalOperator {

	public static void main(String[] args) {
		//Relational Operators (<, >, <=, >=)
		
		
		int x=10;
		int y=12;
		
		System.out.println(x<y); //is 10<12? true
		System.out.println(x>y); //is 10>12? false
		System.out.println(x<=y); //is 10<=12? True
		System.out.println(x>=y); //is 10>=12? False
		
	}

	
	
}
