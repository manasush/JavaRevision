package methodOverloading;

//Method overloading taking place on the same class
//we can take the same method but parameter should be different

public class MethodOverloadingExp {
	String name = "Sushan";
	
	
	
	public void study() {
		
		System.out.println("I study 4 to 5 hours Daily");
		
	}
	
	public void study (int i) {
		
		System.out.println("I study 6 to 7 hours daily");
	}
	
	public static void main(String[] args) {
		
		
	}
	

}


	