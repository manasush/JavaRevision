package datatype;

public class DataTypeDemo2 {

	
	public static void main(String[] args) {
		// Using Float datatype
		
		
		float num1 = 10.5f;
		float num2=30.5f;
		float sum=num1+num2;
		
		System.out.println("Sum of num1 and num2 is: " + sum);
		
		
		//Character data type
		char ch ='m';
		System.out.println("Character = " + ch);
		System.out.println("-------------------");
		
		
		//String type
		//String is a collection of number of characters
		//we can represent it within " ";
		//***later on we will change the definition of it.***
		
		
		String str = "Sushan";
		System.out.println("String: " + str);
		
		System.out.println("-----------");
		
		
		//Boolean datatype
		
		
		boolean b1 = true;
		boolean b2 = false;
		
		
		System.out.println("Boolean =" + b1);
		System.out.println("Boolean =" + b2);
		
		

	}

}
